#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	if (c == '\0')
		return (ft_strlen(s) + (char *)s);
	if (s == NULL)
		return (NULL);
	i = ft_strlen(s);
	while (i > -1)
	{
		if (s[i] == (char)c)
			return ((char *)&s[i]);
		i--;
	}
	return (NULL);
}
